    private void setLatestMovements () {
        LatestMovementsScroll.setHorizontalScrollBarPolicy(JScrollPane.HORIZONTAL_SCROLLBAR_AS_NEEDED); 
        LatestMovementsScroll.setVerticalScrollBarPolicy(JScrollPane.VERTICAL_SCROLLBAR_AS_NEEDED);
        
        LatestMovements.removeAll();
        
        for (Movement movement : session.getBankingAccountList().get(BankingAccountsList.getSelectedIndex()).fetchLatestMovements()) {
            LatestMovements.add(new MovementPanel(movement.getMovementConcept(), movement.getMovementDate(), movement.getMovementTime(), String.valueOf(movement.getMovementAmount())));
            
            System.out.println("Actualizando Lista de cuentas bancarias... " + movement.getMovementConcept());
        }
        
        int height = 1000;
        
        LatestMovements.setPreferredSize(new Dimension(400, height));

        LatestMovements.revalidate();
        LatestMovements.repaint();
        
        LatestMovementsScroll.revalidate();
        LatestMovementsScroll.repaint();
    }
    

    
    /**
     * Modifies CurrentBalance Label with the account balance
     */
    private void setTotalBalance () throws Exception {
        session.setTotalBalance(BankingAccountsList.getSelectedIndex());
        
        double total = session.getTotalBalance();
        CurrentBalanceLabel.setText("<html>Current Balance: " + ((total < 0) ? "<font color='red'>" + total + "</font>" : total) + "</html>");
    }

    private void setBankingAccountList() throws Exception {

        DefaultListModel<String> bankingAccountsList = new DefaultListModel<>();
        session.setBankingAccountList(BankingAccount.fetchBankingAccounts(session.getAccountId()));

        for (BankingAccount bankingAccount : session.getBankingAccountList()) {
            bankingAccountsList.addElement(bankingAccount.fetchBankingAccountName());
        }

        BankingAccountsList.setModel(bankingAccountsList);

        // Selects automatically the first element if it's possible
        if (BankingAccountsList.getModel().getSize() > 0) {
            BankingAccountsList.setSelectedIndex(0);
        }

    }

