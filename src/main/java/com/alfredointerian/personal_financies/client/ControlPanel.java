    private void BankingAccountsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_BankingAccountsListValueChanged
        if (!evt.getValueIsAdjusting()) { // Select only when the user has selected
            System.out.println("Actualizando valores del panel de control..");
            
            session.setBankingAccountSelected(BankingAccountsList.getSelectedIndex());            
            
            try {
                setTotalBalance();
                setLatestMovements();
            } catch (Exception e) {
                System.err.println(e);
            }
            
        }
    }//GEN-LAST:event_BankingAccountsListValueChanged

    private void DeletaBankingAccountBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeletaBankingAccountBtnMouseClicked

        if (JOptionPane.showConfirmDialog(this, "Are you sure about delete '" + session.getBankingAccountList().get(BankingAccountsList.getSelectedIndex()).fetchBankingAccountName() + "' account? This action isn't reversible", "Confirmartion", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            session.getBankingAccountList().get(BankingAccountsList.getSelectedIndex()).deleteBankingAccountInDatabase();

            ControlPanel controlPanel = new ControlPanel();
            controlPanel.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_DeletaBankingAccountBtnMouseClicked

